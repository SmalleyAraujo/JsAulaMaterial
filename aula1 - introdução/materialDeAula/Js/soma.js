window.alert("Seja bem vindo ao script de somar!");
let numero1 = parseInt(window.prompt("Digite o seu primeiro número: ")); 
let numero2 = Number(window.prompt("Digite o seu segundo número: "));

const soma = numero1 + numero2;

// OPERADORES ARITMÉTICOS
// + SOMAR
// - SUBTRAIR
//  * MULTIPLICAR
//  / DIVISÃO
//  ** POTENCIA
//  % RESTO DA DIVISÃO

//OPERADORES LÓGICOS
// &	(and)
// ||	(or)
// !	(not)

window.alert(`A soma dos dois números é de ${soma}`);